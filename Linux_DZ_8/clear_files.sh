#!/bin/bash

# Проверяем, передан ли путь к директории
if [ -z "$1" ]; then
  echo "Ошибка: Пожалуйста, укажите путь к директории."
  exit 1
fi

# Проверяем, существует ли директория
if [ ! -d "$1" ]; then
  echo "Ошибка: Директория $1 не существует."
  exit 1
fi

# Переходим в указанную директорию
cd "$1" || exit 1

# Удаляем файлы с расширениями .bak, .tmp, .backup
find . -type f \( -name "*.bak" -o -name "*.tmp" -o -name "*.backup" \) -exec rm -f {} \;

echo "Директория $1 успешно очищена от файлов с расширениями .bak, .tmp, .backup."
